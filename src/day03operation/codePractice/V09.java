package day03operation.codePractice;
/*赋值运算
 *  ==  +=  -=   *=  /=  %=
 *
 *
 */

public class V09 {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a+=1);// a = a + 1
        System.out.println(a-=1);// a = a - 1
        System.out.println(a==10);//true
        System.out.println(a*=10);// a = a * 10
        System.out.println(a/=10);// a = a / 10
    }
}
