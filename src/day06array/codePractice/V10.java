package day06array.codePractice;

public class V10 {
    public static void main(String[] args) {
        int[] arr={2,6,6,2,5,6,5,98,239,2};
        for(int a :arr){
            System.out.println(a);
        }

        String[] str={"张三丰","宋远桥","俞岱岩","张翠山","吕司机"};
        // 加强for循环 for + alt + / 自动补全
        for (String s : str) {
            System.out.println(s);
        }

    }
}
